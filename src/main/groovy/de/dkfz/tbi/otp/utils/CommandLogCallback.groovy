package de.dkfz.tbi.otp.utils

/**
 * Callback interface for {@link ExecutedCommandLogCallbackThreadLocalHolder}, used to log executed command/script and their result to another place.
 */
interface CommandLogCallback {

    /**
     * used to inform about executed command and the result.
     *
     * @param cmd Holds the command / script, which was executed
     * @param processOutput The result of the command/script
     */
    void executedCommand(String cmd, ProcessOutput processOutput)
}
