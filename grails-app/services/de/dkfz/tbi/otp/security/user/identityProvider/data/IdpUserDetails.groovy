package de.dkfz.tbi.otp.security.user.identityProvider.data

import groovy.transform.Immutable

/**
 * This DTO contains all the relevant user details given by an identity provider
 * (for example, this could be ldap user details or oidc user details).
 */
@Immutable
class IdpUserDetails {
    String username
    String realName
    String mail
    String department
    String thumbnailPhoto
    boolean deactivated
    List<String> memberOfGroupList
}
